<?php
include_once("Product.php");

/**
 * Mouse entity class
 * Class Mouse
 * @package Shop\
 */
class Mouse extends Product
{
    private $model = '';
    private $weight = 0;

    /**
     * @return string
     */
    public function getModel(): string
    {
        return $this->model;
    }

    /**
     * @param string $model
     */
    public function setModel(string $model): void
    {
        $this->model = $model;
    }

    /**
     * @return int
     */
    public function getWeight(): int
    {
        return $this->weight;
    }

    /**
     * @param int $weight
     */
    public function setWeight(int $weight): void
    {
        $this->weight = $weight;
    }


}